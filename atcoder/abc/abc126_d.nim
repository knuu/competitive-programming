const INF = high(int)

proc dfs(v: int, par: int, color: var seq[int], edges: seq[seq[(int, int)]]) =
  for edge in edges[v]:
    let (c, w) = edge
    if c == par:
      continue
    if w mod 2 == 0:
      color[c] = color[v]
    else:
      color[c] = color[v] xor 1
    dfs(c, v, color, edges)


